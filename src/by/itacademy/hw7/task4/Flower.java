package by.itacademy.hw7.task4;

public class Flower {

    private final String color;
    private final int daysOfLife;


    public Flower(String color, int daysOfLife) {
        this.color = color;
        this.daysOfLife = daysOfLife;
    }


    public String getColor() {
        return color;
    }

    public int getDaysOfLife() {
        return daysOfLife;
    }

}
